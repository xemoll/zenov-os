std::vector<std::uint8_t> build(const unsigned char header[64], const unsigned char signature[256], const std::vector<Record>& records) {
    std::vector<std::uint8_t> output(64 + records.size() * sizeof(Record) + 256);
    std::memcpy(output.data(), header, 64); std::memcpy(output.data() + 64, records.data(), records.size() * sizeof(Record));
    std::memcpy(output.data() + output.size() - 256, signature, 256); return output;
}
void write_file(const std::string& path, const std::vector<std::uint8_t>& bytes) {
    std::ofstream output(path, std::ios::binary | std::ios::trunc); if (!output) throw std::runtime_error("cannot open output: " + path);
    output.write(reinterpret_cast<const char*>(bytes.data()), static_cast<std::streamsize>(bytes.size())); if (!output) throw std::runtime_error("cannot write output: " + path);
}
